/*
 Title: How to Write and Analyse an Algorithm
 Author: Inam Ul Haq
 Description:
    The general method of writing an algorithm and the important factors considered 
	while analysing an algorithm.


	How to Write an Algorithm

		1. Understand the Problem:
		- Clearly understand what problem needs to be solved
		- Identify inputs and expected outputs

		2. Define Input and Output:
		- Input: Data given to the algorithm
		- Output: Result produced by the algorithm

		3. Write Step-by-Step Instructions:
		- Steps should be clear and unambiguous
		- Write steps in logical sequence
		- Use simple English statements

		4. Use Standard Keywords:
		- START, STOP
		- READ / INPUT
		- PROCESS / COMPUTE
		- IF, ELSE, LOOP
		- DISPLAY / OUTPUT

		5. Ensure Finiteness:
		- The algorithm must terminate after finite steps

		6. Keep It Language Independent:
		- Do not use programming language syntax
		- Algorithm should be understandable by anyone


	How to Analyse an Algorithm

		1. Time Analysis:
		- Measures how much time an algorithm takes to execute
		- Evaluates efficiency based on number of operations

		2. Space Analysis:
		- Measures how much memory an algorithm uses
		- Includes input space and auxiliary space

		3. Network Consumption / Data Transfer:
		- Analyses data sent or received over a network
		- Important for distributed systems

		4. Power Consumption:
		- Measures energy usage of an algorithm
		- Important for mobile and embedded systems

		5. CPU Register and Resource Usage:
		- Analyses processor registers and hardware resources
		- Helps in low-level performance optimization

	Conclusion:
	    Writing an algorithm focuses on clarity and correctness,
	    while analysing an algorithm focuses on efficiency and
	    resource utilization.
*/
